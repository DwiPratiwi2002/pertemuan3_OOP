public class switchcase {
    
     public static void main(String[] args){
        String nVal = "A";
        
        //control + spasi untuk buat otomatis kalimat ini
         switch (nVal){
             case "A":
                 System.out.println("Nilai nVal adalah A");
                 break;
              case "B":
                 System.out.println("Nilai nVal adalah B");
                 break;
             default:
                 System.out.println("Nilai nVal tidak ada dalam definisi");
         }
    }
}
