public class whileloop {
    public static void main(String[] args){
        
        System.out.println("Perulangan dengan While");
        int cx = 0;
        int batas = 10;
        
        while(cx < batas){ //selama true maka dilakukan perulangan 
            System.out.println("Perulangan ke="+cx);
            cx = cx+4;
        }
    }
}

